import UIKit

class NetworkManager: NSObject {
    
    static let shared = NetworkManager()
    private let cache = NSCache<NSString, UIImage>()
    
    static let baseURL = "https://swapi.dev/api/"
    private let peopleURL = baseURL + "people"
//    private let planetURL = baseURL + "planet"
    
    private override init() {}
    
    
    func getPeople(completed: @escaping (Result<[People], APError>) -> Void) {
        guard let url = URL(string: peopleURL) else {
            completed(.failure(.invalidURL))
            return
        }
               
        let task = URLSession.shared.dataTask(with: URLRequest(url: url)) { data, response, error in
            if let _ =  error {
                completed(.failure(.unableToComplete))
                return
            }
            guard let response = response as? HTTPURLResponse, response.statusCode == 200 else {
                completed(.failure(.invalidResponse))
                return
            }
            guard let data = data else {
                completed(.failure(.invalidData))
                return
            }
            do {
                let decoder = JSONDecoder()
                let decodedResponse = try decoder.decode(PeopleResponse.self, from: data)
                completed(.success(decodedResponse.results))
            } catch {
                completed(.failure(.invalidData))
            }
        }
        task.resume()
    }
    
